/**
 * Encode Mutants & Masterminds 3e conditions into the repo's mam3e
 * conditions data, closing the srd:coverage gap (0/28 -> 28/28).
 *
 * Source: frnprt/mm3e-character-creator js/data.js `CONDITIONS` array
 * (GPL-3.0 code; data derived from the d20herosrd.com M&M 3e SRD, OGL 1.0a —
 * the same repo already wired as the M&M powers/advantages denominator; see
 * docs/srd-sources.md "Mutants & Masterminds 3e"). Each entry is
 * `{ name, description }`.
 *
 * These are GENUINE sourced M&M 3e SRD conditions — NOT placeholders. The
 * conditions/ directory previously shipped only a README (no data); this
 * encoder produces the actual typed catalog the README documents.
 *
 * Categorization (basic vs combined) and superseding relationships follow the
 * M&M 3e SRD as documented in conditions/README.md. The `Condition` interface
 * is emitted inline so the generated index is self-contained.
 *
 * Usage: node scripts/encode-mam-conditions.mjs
 */
import { writeFileSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';
import { execSync } from 'node:child_process';

const __dirname = dirname(fileURLToPath(import.meta.url));

const SOURCE_URL =
  'https://raw.githubusercontent.com/frnprt/mm3e-character-creator/master/js/data.js';

const slug = (name) =>
  name
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-|-$/g, '');

const ts = (value) => JSON.stringify(value, null, 2).replace(/"([a-zA-Z_][a-zA-Z0-9_]*)":/g, '$1:');

// Combined conditions per the M&M 3e SRD (composed of two or more basic
// conditions); everything else is a basic condition. "Normal" is the baseline
// (no condition) and is classified basic, matching README.
const COMBINED = new Set(
  [
    'Asleep',
    'Blind',
    'Bound',
    'Deaf',
    'Dying',
    'Entranced',
    'Exhausted',
    'Incapacitated',
    'Paralyzed',
    'Prone',
    'Restrained',
    'Staggered',
    'Surprised',
  ].map((n) => n.toLowerCase())
);

// Superseding relationships from the M&M 3e SRD (a superseding condition
// replaces the listed weaker condition[s]).
const SUPERSEDED_BY = {
  dazed: ['stunned'],
  compelled: ['controlled'],
  hindered: ['immobile'],
  impaired: ['disabled', 'debilitated'],
  disabled: ['debilitated'],
  fatigued: ['exhausted'],
};

async function main() {
  const res = await fetch(SOURCE_URL);
  if (!res.ok) throw new Error(`Fetch failed: ${res.status}`);
  const text = await res.text();

  const block = text.match(/const CONDITIONS = \[(.*?)\];/s);
  if (!block) throw new Error('CONDITIONS array not found in source');

  const entries = [
    ...block[1].matchAll(/\{\s*name:\s*'([^']+)',\s*description:\s*'((?:[^'\\]|\\.)*)'\s*\}/g),
  ].map((m) => ({ name: m[1], description: m[2].replace(/\\'/g, "'") }));

  if (entries.length === 0) throw new Error('No conditions parsed');

  const conditions = entries.map((e) => {
    const id = slug(e.name);
    const category = COMBINED.has(e.name.toLowerCase()) ? 'combined' : 'basic';
    const cond = {
      id,
      name: e.name,
      system: 'mam3e',
      source: "Hero's Handbook",
      category,
      description: e.description,
      effects: [e.description],
    };
    if (SUPERSEDED_BY[id]) cond.supersededBy = SUPERSEDED_BY[id];
    return cond;
  });

  const varName = (c) => c.id.replace(/-([a-z0-9])/g, (_, ch) => ch.toUpperCase());

  const out = `// GENERATED by scripts/encode-mam-conditions.mjs — DO NOT EDIT BY HAND.
// Source: frnprt/mm3e-character-creator js/data.js CONDITIONS (M&M 3e SRD,
// OGL 1.0a via d20herosrd.com). See docs/srd-sources.md and conditions/README.md.

export interface Condition {
  id: string;
  name: string;
  system: 'mam3e';
  source: string;
  category: 'basic' | 'combined';
  description: string;
  effects: string[];
  supersededBy?: string[];
}

${conditions.map((c) => `export const ${varName(c)}: Condition = ${ts(c)};`).join('\n\n')}

export const basicConditions: Condition[] = [
${conditions
  .filter((c) => c.category === 'basic')
  .map((c) => `  ${varName(c)},`)
  .join('\n')}
];

export const combinedConditions: Condition[] = [
${conditions
  .filter((c) => c.category === 'combined')
  .map((c) => `  ${varName(c)},`)
  .join('\n')}
];

export const allConditions: Condition[] = [...basicConditions, ...combinedConditions];

export const conditionsById: Record<string, Condition> = Object.fromEntries(
  allConditions.map((c) => [c.id, c])
);

export const conditionsByCategory: Record<'basic' | 'combined', Condition[]> = {
  basic: basicConditions,
  combined: combinedConditions,
};
`;

  const target = resolve(__dirname, '../src/data/mutants-and-masterminds/3e/conditions/index.ts');
  writeFileSync(target, out);
  execSync(`npx prettier --write ${JSON.stringify(target)}`, {
    cwd: resolve(__dirname, '..'),
    stdio: 'inherit',
  });
  console.log(`Wrote ${conditions.length} conditions to ${target}`);
}

main();
