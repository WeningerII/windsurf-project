/**
 * Encode Daggerheart SRD 1.0 environments from a local clone of
 * Batres3/daggerheart-srd (DPCGL — the same repo the adversary and domain-card
 * denominators use; see docs/srd-sources.md) into typed DaggerheartEnvironment
 * data.
 *
 * The repo ships one markdown file per environment under
 * environments/Tier {1-4}/, each containing a YAML-ish ```statblock fence with
 * single-line `key: "value"` pairs and a `feats:` list of `- name:`/`desc:`
 * pairs (identical shape to the adversary files).
 *
 * Honest-mapping rules:
 *  - `difficulty` is usually an integer but a few environments print a string
 *    ('Special (see "Relative Strength")'). It is kept verbatim as a string in
 *    that case rather than coerced to NaN — never invented.
 *  - `type` is the environment category (Exploration / Social / Event / Traversal).
 *  - Files that fail field parsing are skipped and REPORTED, never guessed.
 *
 * Usage:
 *   git clone --depth 1 https://github.com/Batres3/daggerheart-srd /tmp/dh-srd
 *   node scripts/encode-dh-environments.mjs --src /tmp/dh-srd
 * (writes src/data/daggerheart/1.0/environments/tier-*.ts + index.ts and
 *  prints a report)
 */

import { readFileSync, readdirSync, writeFileSync, mkdirSync } from 'node:fs';
import { join, resolve } from 'node:path';
import { execSync } from 'node:child_process';

const srcFlag = process.argv.indexOf('--src');
const SRC = srcFlag > -1 ? process.argv[srcFlag + 1] : '/tmp/dh-srd';

const slug = (name) =>
  name
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '');

/** Read `key: "value"` single-line fields out of the statblock fence. */
function field(block, key) {
  const m = new RegExp(`^${key}:\\s*"([^"]*)"\\s*$`, 'm').exec(block);
  return m ? m[1] : undefined;
}

/** Parse the `feats:` list of `- name: "..."` / `desc: "..."` pairs. */
function parseFeats(block) {
  const feats = [];
  const re = /-\s*name:\s*"([^"]*)"\s*\n\s*desc:\s*"([^"]*)"/g;
  for (const m of block.matchAll(re)) {
    feats.push({ name: m[1], description: m[2] });
  }
  return feats;
}

function parseEnvironment(filePath, report) {
  const raw = readFileSync(filePath, 'utf8');
  const fence = /```statblock\n([\s\S]*?)```/.exec(raw);
  if (!fence) {
    report.skipped.push(`${filePath}: no statblock fence`);
    return null;
  }
  const block = fence[1];
  const name = field(block, 'name');
  const tier = Number(field(block, 'tier'));
  const type = field(block, 'type');
  const difficultyRaw = field(block, 'difficulty');
  const features = parseFeats(block);

  if (
    !name ||
    ![1, 2, 3, 4].includes(tier) ||
    !type ||
    difficultyRaw === undefined ||
    features.length === 0
  ) {
    report.skipped.push(
      `${filePath}: name=${name} tier=${tier} type=${type} difficulty=${difficultyRaw} feats=${features.length}`
    );
    return null;
  }

  // Integer difficulty stays numeric; the few 'Special (...)' values are kept
  // verbatim as strings rather than coerced to NaN.
  const numeric = /^\d+$/.test(difficultyRaw.trim());
  const difficulty = numeric ? Number(difficultyRaw.trim()) : difficultyRaw;

  return {
    id: slug(name),
    name,
    system: 'daggerheart',
    source: 'SRD 1.0',
    tier,
    type,
    description: field(block, 'description') ?? '',
    impulses: field(block, 'impulses') ?? '',
    difficulty,
    ...(field(block, 'potential_adversaries')
      ? { potentialAdversaries: field(block, 'potential_adversaries') }
      : {}),
    features,
  };
}

const ts = (value) =>
  JSON.stringify(value, null, 2).replace(/"([a-zA-Z_][a-zA-Z0-9_]*)":/g, '$1:');

function main() {
  const report = { encoded: 0, skipped: [], byTier: {} };
  const outDir = resolve('src/data/daggerheart/1.0/environments');
  mkdirSync(outDir, { recursive: true });

  const indexImports = [];
  const indexSpreads = [];
  for (const tier of [1, 2, 3, 4]) {
    const dir = join(SRC, 'environments', `Tier ${tier}`);
    const environments = [];
    for (const file of readdirSync(dir).sort()) {
      if (!file.endsWith('.md')) continue;
      const env = parseEnvironment(join(dir, file), report);
      if (env) environments.push(env);
    }
    environments.sort((a, b) => a.id.localeCompare(b.id));
    report.byTier[tier] = environments.length;
    report.encoded += environments.length;

    const constName = `daggerheartTier${tier}Environments`;
    const body = environments.map((env) => ts(env)).join(',\n');
    writeFileSync(
      join(outDir, `tier-${tier}.ts`),
      `// GENERATED by scripts/encode-dh-environments.mjs from
// Batres3/daggerheart-srd (Daggerheart SRD 1.0, DPCGL — see
// docs/srd-sources.md). Regenerate per the script header.

import { DaggerheartEnvironment } from './index';

export const ${constName}: DaggerheartEnvironment[] = [
${body},
];
`
    );
    indexImports.push(`import { ${constName} } from './tier-${tier}';`);
    indexSpreads.push(`  ...${constName},`);
    console.log(`tier-${tier}.ts: ${environments.length} environments`);
  }

  writeFileSync(
    join(outDir, 'index.ts'),
    `// GENERATED by scripts/encode-dh-environments.mjs (see tier files).
//
// Daggerheart SRD 1.0 environments (DPCGL). The DaggerheartEnvironment type is
// declared here rather than in src/types/daggerheart.ts to keep this category
// self-contained; promote it to the shared types module when convenient.

import { DaggerheartTier } from '../../../../types/daggerheart';

export interface DaggerheartEnvironment {
  id: string;
  name: string;
  system: 'daggerheart';
  source: string;
  tier: DaggerheartTier;
  /** Environment category, e.g. 'Exploration', 'Social', 'Event', 'Traversal'. */
  type: string;
  description: string;
  /** GM impulses guiding how the environment behaves. */
  impulses: string;
  /**
   * Attack rolls against this environment must meet or beat this. Usually an
   * integer; a few environments print a verbatim string ('Special (...)') whose
   * difficulty derives from another rule, kept as-is rather than invented.
   */
  difficulty: number | string;
  /** Suggested adversaries, verbatim from the SRD (when listed). */
  potentialAdversaries?: string;
  features: Array<{ name: string; description: string }>;
}

${indexImports.join('\n')}

export const daggerheartEnvironments: DaggerheartEnvironment[] = [
${indexSpreads.join('\n')}
];
`
  );

  console.log(
    `\nencoded: ${report.encoded} (by tier: ${JSON.stringify(report.byTier)})`
  );
  console.log(`skipped: ${report.skipped.length}`);
  for (const line of report.skipped) console.log(`  - ${line}`);

  try {
    execSync(`npx prettier --write "${outDir}/*.ts"`, { stdio: 'inherit' });
  } catch (e) {
    console.warn('prettier failed (run manually):', e.message);
  }
}

main();
